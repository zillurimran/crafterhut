            {
                $.ajaxSetup({
                    headers: {
                        'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
                    }
                });

                $.ajax({

                    url  : "{{ route('custom.login') }}", 
                    type : "POST",
                    data : {
                        email : email,
                        password : password,  
                    },
                    success : function(data){
                            if(data.dbError)
                            {
                                $("#passwordError").html(data.dbError);
                            }
                            else
                            {
                                $("#passwordError").html('');
                                $("#loginSubmit").addClass('d-none');
                                $("#spinner").addClass('d-block');
                                setTimeout(function(){
                                    $("#spinner").removeClass('d-none'); 
                                }, 3000); 

                                location.href = "{{ route('dashboard') }}";
                            }
                    }


                });


            }
        });

